export const DEFAULT_CLASS_ID = "fighter";

export function getClassDefinition(classId: string): PlayerClassDefinition | undefined {
  return DND_CLASSES.find((entry) => entry.id === classId);
}

export function getSpellDefinitionById(spellId: string): SpellDefinition | undefined {
  return DND_SPELLS.find((entry) => entry.id === spellId);
}

export function getSpellDefinitionByName(name: string): SpellDefinition | undefined {
  const normalized = name.trim().toLowerCase();
  return DND_SPELLS.find((entry) => entry.name.toLowerCase() === normalized);
}

export function getDefaultSpellsForClass(classId: string): SpellDefinition[] {
  const classDef = getClassDefinition(classId);
  if (!classDef) {
    return [];
  }

  return classDef.suggestedSpellIds
    .map((spellId) => getSpellDefinitionById(spellId))
    .filter((spell): spell is SpellDefinition => Boolean(spell))
    .map((spell) => ({ ...spell }));
}
